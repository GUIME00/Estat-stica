# Uma urna contém 5 bolas vermelhas e 7 azuis. Qual é a probabilidade de retirar uma bola vermelha?
def probabilidade_bola_vermelha():
    bolas_vermelhas = 5
    bolas_azuis = 7
    total_bolas = bolas_vermelhas + bolas_azuis
    probabilidade = bolas_vermelhas / total_bolas
    return probabilidade